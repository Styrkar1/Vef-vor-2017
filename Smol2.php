Hönnunamynstur er kóði sem þú getur notað mörgu sinnum fyrir vandamál sem byrtist oft
en í OOP er það oft notað til að sýna tengingar á milli klassa og object án þess að sýna hvaða klassar eða object eru notuð

<p>-----------------------------------------------------------------</p>

MVC eða Model-View-Controller er hönnunamynstur sem er notað til að búa til user interface
user notar controller sem sendyr upplýsingar u notkunina til Model of model uppfærir view sem sýnir user

<p>-----------------------------------------------------------------</p>

Back end MVC er allt það sem notandinn sér ekki, t.d php,ruby eða eitthver annar server-sided kóði
hann er líka aðanlega noaður til að stjórna upplýsingum og hann er mikið flóknari því að það er svo mikið meira en bara að sýna notandanum í gangi

<p>-----------------------------------------------------------------</p>

Ég skil það þannig að MV* er það sem virkar aðeins öðruvísi frá MVC í þannig æleið að það er ekki endilega Model/View/controller
T.d Model-View-Presenter(MVP) og Model-View-ViewModel (MVVM)